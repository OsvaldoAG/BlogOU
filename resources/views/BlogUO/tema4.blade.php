Computadoras centrales:
<br>Computadora central o También conocida como mainframe, es una computadora grande, potente y costosa, usada principalmente por una gran compañía para el procesamiento de una gran cantidad de datos; por ejemplo, para el procesamiento de transacciones bancarias.
<br>Caracteristicas:
<br> •	Vel. de proceso: Cientos de millones de instrucciones por segundo o mas.
<br> •	Usuario a la vez: Centenares o miles.
<br> •	Tamaño: Requieren instalaciones especiales y aire acondicionado.
<br> •	Facilidad de uso: Para especialistas.
<br> •	Clientes usuales: Grandes corporaciones y gobiernos.
<br> •	Penetración social: Baja.
<br> •	Impacto social: Muy alto, aunque pasa inadvertido, la sociedad industrial moderna no puede funcionar sin ellas.
<br> •	Parque instalado: Miles en todo el mundo.
<br> •	Costo: Centenares de miles de dólares o más.
<br>Computadoras dedicadas:
<br>Se trata de un modelo que puede ser tanto para sistemas (como Business Intelligence o ERP) como para sitios en que la empresa asegura un servidor totalmente exclusivo en el data center. Esto quiere decir que se garantiza la total personalización de las necesidades, desde el espacio de almacenamiento hasta el tamaño de memoria RAM y velocidad de procesamiento.
<br>Ventajas:
<br> •	Performance
<br> El servidor dedicado favorece la performance de aplicaciones y de sitios web. Como se trata de una solución totalmente exclusiva, el tráfico tiende a ser bien menor cuando comparado al ambiente compartido, por ejemplo. Esto, sin dudas, contribuye para la navegación, aparte de reducir el tiempo de carga.  Siendo así, el servidor dedicado es capaz de reducir significativamente la lentitud de sitios y de sistemas, contribuyendo para la confiabilidad de los visitantes y de los usuarios.  
<br> •	Flexibilidad 
<br> Con esta solución, se tiene total control del ambiente de hosting y total libertad para instalar scripts, módulos y plugins. Esto no sucede con los hostings compartidos, una vez que no permiten tanta flexibilidad para los usuarios.  El servidor dedicado contribuye también para la escalabilidad de proyectos que tienen un crecimiento muy rápido o picos de accesos.  
<br> •	Autonomía 
<br> Otro beneficio ofrecido por este servidor es la autonomía proporcionada para el equipo de Tecnología de Información (TI) o para el desarrollador, sea para el control de accesos, o para la gestión del banco de datos. Con él, es posible tener acceso a la línea de comando como root. 
<br> •	Estabilidad 
<br> La estabilidad es otro factor relevante del servidor dedicado, que tiende a ser superior por ser exclusivo.  Los servidores dedicados acostumbran ubicarse en data centers de primera línea, contando con los mejores recursos de redes y de hardware. Por eso, la infraestructura garantiza más condiciones de desempeño y de estabilidad para los clientes.  
<br> •	Seguridad 
<br> La preocupación con la seguridad es menor para quien elige un servidor dedicado, especialmente, por el hecho de que los accesos limitados pueden ser personalizados de acuerdo con la necesidad. Esto sin contar que, en él, es posible bloquear IPs, entre otras medidas de seguridad.  

