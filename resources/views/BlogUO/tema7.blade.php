@section('introduccion')
Un protocolo es un conjunto de reglas: los protocolos de red son estándares y políticas formales,
<br> conformados por restricciones, procedimientos y formatos que definen el intercambio de paquetes de información para lograr la comunicación entre dos servidores o más dispositivos a través de una red.
<br>Existen varios tipos de protocolos de red:

<br>Protocolos de comunicación de red: protocolos de comunicación de paquetes básicos como TCP / IP y HTTP.
<br>Protocolos de seguridad de red: implementan la seguridad en las comunicaciones de red entre servidores, incluye HTTPS, SSL y SFTP.
<br>Protocolos de gestión de red: proporcionan mantenimiento y gobierno de red, incluyen SNMP e ICMP.
<br>Un grupo de protocolos de red que trabajan juntos en los niveles superior e inferior comúnmente se les denomina familia de protocolos.
