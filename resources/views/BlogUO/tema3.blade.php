@section('introduccion')
La arquitectura cliente-servidor es un modelo de diseño de software en el que las tareas se reparten entre los proveedores de recursos o servicios, llamados servidores, y los demandantes, llamados clientes. Un cliente realiza peticiones a otro programa, el servidor, quien le da respuesta.
<br>Caracteristicas: 
<br> •	Es quien inicia solicitudes o peticiones, tiene por tanto un papel activo en la comunicación (dispositivo maestro o amo).
<br> •	Espera y recibe las respuestas del servidor.
<br> •	Por lo general, puede conectarse a varios servidores a la vez.
<br> •	Normalmente interactúa directamente con los usuarios finales mediante una interfaz gráfica de usuario.

